package com.misc;

public class CardAcceptorInfo {

    byte[] name;
    byte[] city;
    String stateTypeCode;
    String postalCode;
    String countryTypeCode;
    String phoneNumber;
    String urlAddress;
    String emailAddress;
    String cityCode;

    public byte[] getName() {
        return name;
    }

    public void setName(byte[] name) {
        this.name = name;
    }

    public byte[] getCity() {
        return city;
    }

    public void setCity(byte[] city) {
        this.city = city;
    }

    public String getStateTypeCode() {
        return stateTypeCode;
    }

    public void setStateTypeCode(String stateTypeCode) {
        this.stateTypeCode = stateTypeCode;
    }

    public String getPostalCode() {
        return postalCode;
    }

    public void setPostalCode(String postalCode) {
        this.postalCode = postalCode;
    }

    public String getCountryTypeCode() {
        return countryTypeCode;
    }

    public void setCountryTypeCode(String countryTypeCode) {
        this.countryTypeCode = countryTypeCode;
    }

    public String getPhoneNumber() {
        return phoneNumber;
    }

    public void setPhoneNumber(String phoneNumber) {
        this.phoneNumber = phoneNumber;
    }

    public String getUrlAddress() {
        return urlAddress;
    }

    public void setUrlAddress(String urlAddress) {
        this.urlAddress = urlAddress;
    }

    public String getEmailAddress() {
        return emailAddress;
    }

    public void setEmailAddress(String emailAddress) {
        this.emailAddress = emailAddress;
    }

    public String getCityCode() {
        return cityCode;
    }

    public void setCityCode(String cityCode) {
        this.cityCode = cityCode;
    }
}
